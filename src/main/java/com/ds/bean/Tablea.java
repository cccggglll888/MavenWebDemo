package com.ds.bean;

/**
 * 与tablea对应的实体类
 */
public class Tablea {
	private String cola;
	private String colb;
	private String colc;
	private String cold;
	
	public String getCola() {
		return cola;
	}
	public void setCola(String cola) {
		this.cola = cola;
	}
	public String getColb() {
		return colb;
	}
	public void setColb(String colb) {
		this.colb = colb;
	}
	public String getColc() {
		return colc;
	}
	public void setColc(String colc) {
		this.colc = colc;
	}
	public String getCold() {
		return cold;
	}
	public void setCold(String cold) {
		this.cold = cold;
	}
	
}
